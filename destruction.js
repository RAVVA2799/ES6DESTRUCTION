// we have an array with the name and surname
let arr = ["John", "Smith"]       // 2 VALUES IN AN ARRAY

// destructuring assignment
// sets firstName = arr[0]
// and surname = arr[1]
let [firstName, surname] = arr;       //VALUES ASSIGNED IN NAMES.

console.log(firstName); // John      //JOHN &SMITH WILL BE
console.log(surname);  // Smith     //SEPATERLY PRINTED